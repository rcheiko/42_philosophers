#include "philosophers.h"

int	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	char_digit(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
